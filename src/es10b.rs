/// ES10b.ListNotification response.
///
/// # Errors
///
/// Decoding returns [`EuiccError`] for malformed notification entries or error
/// result codes.
#[derive(Debug, Clone, PartialEq, Eq, Default)]
pub struct ListNotificationResponse {
    /// Notification metadata entries.
    pub notifications: Vec<NotificationMetadata>,
}

impl DecodeTlv for ListNotificationResponse {
    fn from_tlv(tlv: &Tlv) -> Result<Self> {
        ensure_tag(tlv, 40, "listNotificationResponse")?;
        if let Some(error) = tlv.first(&Class::ContextSpecific.primitive(1)) {
            return match decode_i64(error.value().unwrap_or_default(), 1)? {
                127 => Err(EuiccError::Undefined),
                _ => Err(EuiccError::Undefined),
            };
        }
        let Some(choice) = tlv.first(&Class::ContextSpecific.constructed(0)) else {
            let Some(child) = tlv.children().unwrap_or_default().first() else {
                return Ok(Self::default());
            };
            return Err(EuiccError::UnexpectedTag {
                expected: "notificationMetadataList",
                actual: child.tag().hex(),
            });
        };

        let mut notifications = Vec::new();
        for child in choice.children().unwrap_or_default() {
            if child
                .tag()
                .is(Class::ContextSpecific, Form::Constructed, 47)
            {
                notifications.push(NotificationMetadata::from_tlv(child)?);
            }
        }
        Ok(Self { notifications })
    }
}

impl ListNotificationResponse {
    /// Decodes the response from a TLV.
    ///
    /// # Errors
    ///
    /// Returns [`EuiccError`] when entries are malformed or the response carries
    /// an error result.
    pub fn from_tlv(tlv: &Tlv) -> Result<Self> {
        <Self as DecodeTlv>::from_tlv(tlv)
    }
}

impl CardResponse for ListNotificationResponse {}

