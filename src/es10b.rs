/// ES10b.LoadBoundProfilePackage response.
///
/// # Errors
///
/// Decoding returns [`EuiccError`] when mandatory fields are absent.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct LoadBoundProfilePackageResponse {
    /// Transaction id returned in ProfileInstallationResult.
    pub transaction_id: HexBytes,
    /// Notification metadata returned by the card.
    pub notification: NotificationMetadata,
    /// Final result TLV.
    pub final_result: Tlv,
}

impl DecodeTlv for LoadBoundProfilePackageResponse {
    fn from_tlv(tlv: &Tlv) -> Result<Self> {
        ensure_tag(tlv, 55, "profileInstallationResult")?;
        let result = tlv
            .first(&Class::ContextSpecific.constructed(39))
            .ok_or(EuiccError::MissingField("profileInstallationResultData"))?;
        let transaction_id = HexBytes::from_bytes(required_value(
            result,
            Class::ContextSpecific.primitive(0),
            "transactionId",
        )?);
        let final_result = result
            .first(&Class::ContextSpecific.constructed(2))
            .ok_or(EuiccError::MissingField("finalResult"))?
            .clone();
        let notification = NotificationMetadata::from_tlv(
            result
                .first(&Class::ContextSpecific.constructed(47))
                .ok_or(EuiccError::MissingField("notificationMetadata"))?,
        )?;
        Ok(Self {
            transaction_id,
            notification,
            final_result,
        })
    }
}

impl LoadBoundProfilePackageResponse {
    /// Decodes the response from a TLV.
    ///
    /// # Errors
    ///
    /// Returns [`EuiccError`] when mandatory fields are absent or malformed.
    pub fn from_tlv(tlv: &Tlv) -> Result<Self> {
        <Self as DecodeTlv>::from_tlv(tlv)
    }

    /// Returns the installed ISD-P AID when the final result is successful.
    #[must_use]
    pub fn isdp_aid(&self) -> Option<IsdpAid> {
        let success = self
            .final_result
            .first(&Class::ContextSpecific.constructed(0))?;
        let aid = success.first(&Class::Application.primitive(15))?.value()?;
        Some(IsdpAid::from_bytes(aid.to_vec()))
    }
}

impl CardResponse for LoadBoundProfilePackageResponse {
    fn validate(&self) -> Result<()> {
        let Some(result) = self
            .final_result
            .first(&Class::ContextSpecific.constructed(1))
        else {
            return Ok(());
        };
        let command_id = BppCommandId::from_i64(required_int(
            result,
            Class::ContextSpecific.primitive(0),
            "bppCommandId",
            1,
        )?);
        let reason = BppErrorReason::from_i64(required_int(
            result,
            Class::ContextSpecific.primitive(1),
            "bppErrorReason",
            1,
        )?);
        Err(LoadBoundProfilePackageError::new(command_id, reason).into())
    }
}

