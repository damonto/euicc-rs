/// ES10b.PrepareDownload request.
///
/// # Errors
///
/// Encoding returns [`EuiccError::MissingField`] when a confirmation code is
/// required by `smdpSigned2` but not provided.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct PrepareDownloadRequest {
    /// Transaction id generated by the RSP server.
    pub transaction_id: HexBytes,
    /// Profile metadata from ES9+.AuthenticateClient when present.
    pub profile_metadata: Option<Tlv>,
    /// SM-DP+ signed2 TLV.
    pub signed2: Tlv,
    /// SM-DP+ signature2 TLV.
    pub signature2: Tlv,
    /// SM-DP+ certificate TLV.
    pub certificate: Tlv,
    /// Optional confirmation code entered by the user.
    pub confirmation_code: Option<Vec<u8>>,
}

impl PrepareDownloadRequest {
    /// Returns true when `smdpSigned2` requires a confirmation code.
    #[must_use]
    pub fn need_confirmation_code(&self) -> bool {
        self.signed2
            .first(&Class::Universal.primitive(1))
            .and_then(Tlv::value)
            .is_some_and(decode_bool)
    }

    /// Computes the SGP.22 hashCc value.
    #[must_use]
    pub fn hashed_confirmation_code(&self) -> Option<Vec<u8>> {
        if !self.need_confirmation_code() {
            return None;
        }
        let code = self.confirmation_code.as_ref()?;
        let first = Sha256::digest(code);
        let mut hasher = Sha256::new();
        hasher.update(first);
        hasher.update(self.transaction_id.as_bytes());
        Some(hasher.finalize().to_vec())
    }
}

impl EncodeTlv for PrepareDownloadRequest {
    fn to_tlv(&self) -> Result<Tlv> {
        if self.need_confirmation_code() && self.confirmation_code.is_none() {
            return Err(EuiccError::MissingField("confirmationCode"));
        }
        let mut children = Vec::with_capacity(4);
        children.push(self.signed2.clone());
        children.push(self.signature2.clone());
        if let Some(hash) = self.hashed_confirmation_code() {
            children.push(Tlv::primitive(Class::Universal.primitive(4), hash)?);
        }
        children.push(self.certificate.clone());
        constructed(33, children)
    }
}

impl CardRequest for PrepareDownloadRequest {
    type Response = PrepareDownloadResponse;

    fn decode_response(&self, tlv: &Tlv) -> Result<Self::Response> {
        PrepareDownloadResponse::from_tlv(tlv)
    }
}

