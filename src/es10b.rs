/// ES10b.AuthenticateServer request.
///
/// # Errors
///
/// Encoding returns [`EuiccError::MissingField`] when the IMEI is too short for
/// the TAC field.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct AuthenticateServerRequest {
    /// Transaction id generated by the RSP server.
    pub transaction_id: HexBytes,
    /// ServerSigned1 TLV.
    pub signed1: Tlv,
    /// Server signature TLV.
    pub signature1: Tlv,
    /// CI public key identifier TLV to be used.
    pub used_issuer: Tlv,
    /// Server certificate TLV.
    pub certificate: Tlv,
    /// Device IMEI in swapped BCD.
    pub imei: Imei,
    /// Optional MatchingId text.
    pub matching_id: Option<String>,
}

impl EncodeTlv for AuthenticateServerRequest {
    fn to_tlv(&self) -> Result<Tlv> {
        let imei = self.imei.as_bytes();
        if imei.len() != 4 && imei.len() != 8 {
            return Err(EuiccError::MalformedTlv("imei must be 4 or 8 bytes"));
        }
        let device_info = Tlv::constructed(
            Class::ContextSpecific.constructed(1),
            device_info_children(imei)?,
        )?;
        let ctx_children = vec![
            Tlv::primitive(
                Class::ContextSpecific.primitive(0),
                self.matching_id.as_deref().unwrap_or("").as_bytes(),
            )?,
            device_info,
        ];
        let ctx_params = Tlv::constructed(Class::ContextSpecific.constructed(0), ctx_children)?;
        constructed(
            56,
            vec![
                self.signed1.clone(),
                self.signature1.clone(),
                self.used_issuer.clone(),
                self.certificate.clone(),
                ctx_params,
            ],
        )
    }
}

impl CardRequest for AuthenticateServerRequest {
    type Response = AuthenticateServerResponse;

    fn decode_response(&self, tlv: &Tlv) -> Result<Self::Response> {
        AuthenticateServerResponse::from_tlv(tlv)
    }
}

